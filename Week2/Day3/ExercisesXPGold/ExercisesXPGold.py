    # ĞœĞ¸Ğ½Ğ¸Ğ¼ÑƒĞ¼ 1 ÑÑ‚Ñ€Ğ¾Ñ‡Ğ½Ğ°Ñ Ğ±ÑƒĞºĞ²Ğ°
    if not re.search(r'[a-z]', password):
        is_valid = False
        
    # ĞœĞ¸Ğ½Ğ¸Ğ¼ÑƒĞ¼ 1 Ğ·Ğ°Ğ³Ğ»Ğ°Ğ²Ğ½Ğ°Ñ Ğ±ÑƒĞºĞ²Ğ°
    if not re.search(r'[A-Z]', password):
        is_valid = False
        
    # ĞœĞ¸Ğ½Ğ¸Ğ¼ÑƒĞ¼ 1 ÑĞ¿ĞµÑ†Ğ¸Ğ°Ğ»ÑŒĞ½Ñ‹Ğ¹ ÑĞ¸Ğ¼Ğ²Ğ¾Ğ»
